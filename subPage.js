function createimg(imgUrl) {
  const imgContainer = document.createElement('div');
  imgContainer.className = 'img-container';
  imgContainer.style.display = 'none'; // Initially hide all imgs
  const img = document.createElement('img');
  img.classList.add('img')
  console.log(imgUrl)
  img.src = imgUrl;
  imgContainer.appendChild(img);
  return imgContainer;
}

function renderCarousel() {
  const carousel = document.getElementById('carousel');
  carousel.innerHTML = ''; 
  if(imgS == 1){
    active=0
    const imgUrl = `VERTICAL/${id}.JPG`; // Adjust path to your image
    const img = createimg(imgUrl);
    carousel.appendChild(img);
  }else{
    for (let i = 0; i < imgS; i++) {
        const imgUrl = `VERTICAL/${id}_${i}.JPG`; // Adjust path to your image
        const img = createimg(imgUrl);
        carousel.appendChild(img);
      }
  }
  
  setActive(active); // Show active img
}

function setActive() {
  const imgs = document.querySelectorAll('.img-container');
  imgs.forEach((img, i) => {
    const offset = (active - i) / 3;
    const absOffset = Math.abs(active - i) / 3;
    img.style.setProperty('--active', i === active ? 1 : 0);
    img.style.setProperty('--offset', (active - i) / 3);
    img.style.setProperty('--direction', Math.sign(active - i));
    img.style.setProperty('--abs-offset', absOffset);
    img.style.opacity = Math.abs(active - i) >= MAX_VISIBILITY ? '0' : '1';
    img.style.display = Math.abs(active - i) > MAX_VISIBILITY ? 'none' : 'block';
    img.style.pointerEvents = active === i ? 'auto' : 'none';
  });
}

